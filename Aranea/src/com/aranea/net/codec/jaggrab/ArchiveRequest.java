package com.aranea.net.codec.jaggrab;

public enum ArchiveRequest {

    TITLE("title", 1),
    CONFIG("config", 2),
    INTERFACE("interface", 3),
    MEDIA("media", 4),
    VERSION_LIST("versionlist", 5),
    TEXTURES("textures", 6),
    WORDENC("wordenc", 7),
    SOUNDS("sounds", 8);

    private String name;
    private int index;

    ArchiveRequest(String name, int index) {
        this.name = name;
        this.index = index;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
